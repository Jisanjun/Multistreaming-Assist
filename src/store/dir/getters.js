export function dir(state) {
    return state.dir
}

export function rootDir(state) {
    return state.rootDir
}

export function nginxDir(state) {
    return state.nginxDir
}

export function nginxConfDir(state) {
    return state.nginxConfDir
}

export function nginxLogsDir (state) {
    return state.nginxLogsDir
}

export function recordingDir(state) {
    return state.recordingDir.toString()
}